const number2Lcd = function(n) {
  const lcd = [
    ["._.", "|.|", "|_|"],
    ["...", "..|", "..|"],
    ["._.", "._|", "|_."],
    ["._.", "._|", "._|"],
    ["...", "|_|", "..|"],
    ["._.", "|_.", "._|"],
    ["._.", "|_.", "|_|"],
    ["._.", "..|", "..|"],
    ["._.", "|_|", "|_|"],
    ["._.", "|_|", "..|"]
  ];
  return n
    .split("")
    .map(v => lcd[v])
    .reduce((acc, cur) => [
      acc[0] + " " + cur[0],
      acc[1] + " " + cur[1],
      acc[2] + " " + cur[2]
    ])
    .map(v => v + "\n")
    .join("");
};
console.log(number2Lcd("960"));
